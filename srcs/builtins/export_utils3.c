#include "minishell.h"

t_bool	has_plus(char *s)
{
	int	i;

	if (!s)
		return (FALSE);
	i = -1;
	while (s[++i + 1])
		if (s[i] == '+' && s[i + 1] == '=')
			return (TRUE);
	return (FALSE);
}
